def run_tests():
    """运行所有测试用例"""
    cities_db = load_city_database()
    print(f"数据库加载完成：{len(cities_db)} 个城市")
    print()

    total = 0
    passed = 0
    failed_cases = []

    for expected_id, queries in TEST_CASES:
        for query in queries:
            total += 1
            results = fuzzy_match(query, cities_db)
            if results and results[0]['id'] == expected_id:
                passed += 1
            else:
                actual_id = results[0]['id'] if results else "None"
                actual_name = results[0]['name'] if results else ""
                expected_name = ""
                for city in cities_db:
                    if city['id'] == expected_id:
                        expected_name = city['name']
                        break
                failed_cases.append({
                    'query': query,
                    'expected': f"{expected_name} ({expected_id})",
                    'actual': f"{actual_name} ({actual_id})"
                })

    print(f"测试完成：{passed}/{total} 通过 ({passed/total*100:.1f}%)")

    if failed_cases:
        print(f"失败用例 ({len(failed_cases)} 个)：")
        for case in failed_cases:
            print(f"  查询: {case['query']}")
            print(f"    期望: {case['expected']}")
            print(f"    实际: {case['actual']}")

    return len(failed_cases) == 0


if __name__ == '__main__':
    success = run_tests()
    sys.exit(0 if success else 1)
