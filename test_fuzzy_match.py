def run_tests():
    """运行所有测试用例"""
    print("=" * 60)
    print("  城市模糊匹配测试")
    print("=" * 60)
    print()
    
    # 加载数据库
    cities_db = load_city_database()
    print(f"数据库加载完成：{len(cities_db)} 个城市")
    print()
    
    # 统计
    total = 0
    passed = 0
    failed_cases = []
    
    for expected_name, expected_id, queries in TEST_CASES:
        for query in queries:
            total += 1
            results = fuzzy_match(query, cities_db)
            
            if results and results[0]['id'] == expected_id:
                passed += 1
            else:
                actual_id = results[0]['id'] if results else "未找到"
                actual_name = results[0]['name'] if results else ""
                failed_cases.append({
                    'query': query,
                    'expected': f"{expected_name} ({expected_id})",
                    'actual': f"{actual_name} ({actual_id})"
                })
    
    # 输出结果
    print(f"测试完成：{passed}/{total} 通过")
    print()
    
    if failed_cases:
        print(f"失败用例 ({len(failed_cases)} 个)：")
        print("-" * 60)
        for case in failed_cases:
            print(f"  查询: {case['query']}")
            print(f"    期望: {case['expected']}")
            print(f"    实际: {case['actual']}")
            print()
    
    # 按城市统计
    print("=" * 60)
    print("各城市测试结果：")
    print("-" * 60)
    
    city_results = {}
    for expected_name, expected_id, queries in TEST_CASES:
        city_pass = 0
        city_total = len(queries)
        for query in queries:
            results = fuzzy_match(query, cities_db)
            if results and results[0]['id'] == expected_id:
                city_pass += 1
        city_results[expected_name] = (city_pass, city_total)
    
    # 输出每个城市的结果
    for city_name, (pass_count, total_count) in city_results.items():
        status = "PASS" if pass_count == total_count else "FAIL"
        print(f"  {status} {city_name}: {pass_count}/{total_count}")
    
    print()
    return len(failed_cases) == 0


if __name__ == '__main__':
    success = run_tests()
    sys.exit(0 if success else 1)
